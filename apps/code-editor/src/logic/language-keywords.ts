/** Languages whose reserved words help completion. Data / markup formats
 *  (JSON, YAML, TOML, HTML, Markdown, Dockerfile, plain text) carry no useful
 *  keyword set — they fall through to buffer-only completion. */
const KEYWORDS: Partial<Record<LanguageKey, readonly string[]>> = {
	[LanguageKey.JavaScript]: JS_KEYWORDS,
	[LanguageKey.JSX]: JS_KEYWORDS,
	[LanguageKey.TypeScript]: TS_KEYWORDS,
	[LanguageKey.TSX]: TS_KEYWORDS,
	[LanguageKey.Python]: PYTHON_KEYWORDS,
	[LanguageKey.Rust]: RUST_KEYWORDS,
	[LanguageKey.Go]: GO_KEYWORDS,
	[LanguageKey.Java]: JAVA_KEYWORDS,
	[LanguageKey.Shell]: SHELL_KEYWORDS,
	[LanguageKey.SQL]: SQL_KEYWORDS,
};

const NONE: readonly string[] = Object.freeze([]);

/** The reserved-word completion set for a language, or an empty list for the
 *  data / markup / unknown languages that have none. */
export function keywordsForLanguage(language: LanguageKey): readonly string[] {
	return KEYWORDS[language] ?? NONE;
}
